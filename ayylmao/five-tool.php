            <h2>The Five-Tool Model</h2>
            <p>The 5 tool model is a concept derived from the 5 different tools in baseball required to become a successful player. The tools are Running Speed, Arm Strength, Hitting for Average, Hitting for Power, and Fielding. Different tools vary for different positions and situations. Every prospect is evaluated, rated, and compared in the our network.</p>

            <h3>Running Speed</h3>
            <p>Running Speed is one of the most attractive tools to scouts. Players who run well are known for making things happen on the base paths and putting pressure on the defense. While genetics is a large factor in a player's speed, our trainers will still work specifically in this area to help a player reach their full potential.</p>

            <h3>Arm Strength</h3>
            <p>Arm strength plays a vital role in the evaluation of a prospect, whether it is a pitcher having the muscle endurance to consistently throw over a hundred pitches per outing, or a shortstop having the ability to go deep into the hole (area between 3rd Baseman and Shortstop) and having a strong enough arm to throw the runner out. Catchers must have a strong arm to hold base runners at bay and control the tempo of a game.</p>

            <h3>Hitting for Average</h3>
            <p>Hitting for average is a key component to the offense, a player must have the plate discipline and barrel control to be able to put the ball in all areas of the field; an offense that does not hit for average will not win many games. Our trainers work with prospects to teach hitting concepts while in the box, how to work a count, and dissect their swings and correct any flaws.</p>

            <h3>Hitting for Power</h3>
            <p>The most attractive attribute any prospect can have is hitting for power. Games are won by putting base-runners on and driving the ball off the wall or over the fence. Hitting for power comes from a strong weight training regimen, and consistently working with a prospect's swing. We have specific training programs geared toward hitting power. A strong core and legs correlates to a strong fluent powerful swing.</p>

            <h3>Fielding</h3>
            <p>Good fielding results in a player’s ability to play defense on a ball, whether it’s an outfielder’s great first step to tracking down an out, or an infielder's “soft hands” to turn a double play. Fielding techniques and fundamentals are at the forefront of a successful position player. Our trainers can work one-on-one with a prospect on the defensive fundamentals of their position.</p>
